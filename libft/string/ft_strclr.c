#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	t;

	if (s == NULL)
		return ;
	t = ft_strlen(s);
	i = 0;
	while (i < t)
	{
		s[i] = '\0';
		i++;
	}
	s[i] = '\0';
}
