#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	i;
	size_t	l;

	if (s == NULL || f == NULL)
		return ;
	i = 0;
	l = ft_strlen(s);
	while (i < l)
	{
		f(&s[i]);
		i++;
	}
}
