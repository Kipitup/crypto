/*
** The round constant word array, rcon[i], contains the values given by 
** x to the power (i-1) in hexadecimalx
*/
static const uint8_t rcon[11] = {
  0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36 };

/*
** word == 4 bytes == 32 bits
*/
void			print_word(uint8_t *word, size_t start, size_t end)
{
	size_t		i;
	size_t		j;
	size_t		k;

	i = 0;
	while (i < 4)
	{
		j = start;
		while (j <= end)
		{
			if (j % 4 == 0)
				ft_dprintf(STD_ERR, " ");
			k = j * 4 + i;
			ft_dprintf(STD_ERR, "|{c_yellow}%0.2X{c_end}|", word[k]);
			j++;
		}
		ft_dprintf(STD_ERR, "\n");
		i++;
	}
	ft_dprintf(STD_ERR, "\n");
}

/*
** RotWord performs a one-byte circular left shift on a word. This means that
** an input word [B0, B1, B2, B3] is transformed into [B1, B2, B3, B0].
*/
static void		rot_word(uint8_t *word)
{
	const uint8_t	tmp = word[0];

	word[0] = word[1];
	word[1] = word[2];
	word[2] = word[3];
	word[3] = tmp;
}

/*
** SubWord performs a byte substitution on each byte of its input word,
** using the S-box
*/
static void		sub_word(uint8_t *word)
{
	word[0] = sbox[word[0]];  
	word[1] = sbox[word[1]];  
	word[2] = sbox[word[2]];  
	word[3] = sbox[word[3]];  
}

/*
** At the end tmp is XORed with a round constant, rcon[i]. Since the three
** rightmost bytes are always 0 in the round constant. We only XOR the 1st byte.
** 
** Note that (i / 4) > 0 so rcon[0] will never be used.
*/
static void		aes_g_func(uint8_t *tmp, size_t i)
{
	rot_word(tmp);
	sub_word(tmp);
	tmp[0] = tmp[0] ^ rcon[i / 4];
}

/*
** word == 4 bytes == 32 bits
**
** The first 4 words of the round_key are the key itself.
** Then each word depend on the previous word and the word 4 position back.
*/
void			key_expansion(uint8_t *key, uint8_t *round_key)
{
	size_t		i;
	size_t		prev;
	uint8_t		tmp[4];

	i = 4;
	ft_memcpy(round_key, key, 16);
	while (i < 44)
	{
		prev = (i - 1) * 4;
		ft_memcpy(tmp, round_key + prev, 4);
		if (i % 4 == 0)
			aes_g_func(tmp, i);
		prev = (i - 4) * 4;
		round_key[i * 4 + 0] = round_key[prev + 0] ^ tmp[0];
		round_key[i * 4 + 1] = round_key[prev + 1] ^ tmp[1];
		round_key[i * 4 + 2] = round_key[prev + 2] ^ tmp[2];
		round_key[i * 4 + 3] = round_key[prev + 3] ^ tmp[3];
		i++;
	}
}

int			main()
{
	uint8_t key[16] = {0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c};
	uint8_t round_key[176];

	print_word(key, 0, 3);
	key_expansion(key, round_key);
	print_word(round_key, 0, 43);
	return (0);
}
